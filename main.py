# Asks for sum

n = int(input("First Number: "))

s =input("Symbol: ")

n2 = int(input("Second Number: "))

# ----------------------------------------------------

# Division

if s == "/":

	 print(n, "/", n2, "=", n / n2)	 

# Addition 

if s == "+":

	 print(n, "+", n2, "=", n + n2 )

# Subtraction 

if s == "-":

	 print(n, "-", n2, "=", n - n2)

# Multiplication 

if s == "*":

	 print(n, "*", n2, "=", n * n2)
